    private void init(Player player)
    {
        this.player = player;
        lblName.setText(player.toString());
        txtBalance.setText(Global.DOLLARS.format(player.getBalance()));
        txtPayment.setText(Global.DOLLARS.format(player.getBalance()));
        txtPayment.requestFocus();
        txtPayment.selectAll();
    }
    
    private void confirmReceipt()
    {
        if (new String(pwdAuthenticate.getPassword()).equals(Global.AUTHENTICATION_PASSWORD))
        {
            String memo = txtMemo.getText();
            double amount = Global.normalizeMoneyAmount(txtPayment.getText());
            Transact payment = Transact.makePayment(player, amount, memo);
            player.updateToDatabase();
            JOptionPane.showMessageDialog(this, "Authentication succeeded.", "Confirmation", JOptionPane.INFORMATION_MESSAGE);
            setVisible(false);
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Authentication failed.", "Error", JOptionPane.ERROR_MESSAGE);
            pwdAuthenticate.setText("");
            pwdAuthenticate.requestFocus();
        }
    }
    
