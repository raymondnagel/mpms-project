    private void txtPaymentFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtPaymentFocusLost
        try {
            double payment = Double.parseDouble(txtPayment.getText());
            if (payment < 0)
            {
                if (txtMemo.getText().equalsIgnoreCase("payment"))
                {
                    txtMemo.setText("charge");
                }
            }
            else
            {
                if(txtMemo.getText().equalsIgnoreCase("charge"))
                {
                    txtMemo.setText("payment");
                }
            }
        } catch (Exception ex)
        {
        }
    }//GEN-LAST:event_txtPaymentFocusLost

